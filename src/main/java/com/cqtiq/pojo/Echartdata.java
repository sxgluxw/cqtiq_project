package com.cqtiq.pojo;

public class Echartdata {
    private Integer id;

    private Double tach;

    private Double h;

    private Double gas;

    private Double water;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Double getTach() {
        return tach;
    }

    public void setTach(Double tach) {
        this.tach = tach;
    }

    public Double getH() {
        return h;
    }

    public void setH(Double h) {
        this.h = h;
    }

    public Double getGas() {
        return gas;
    }

    public void setGas(Double gas) {
        this.gas = gas;
    }

    public Double getWater() {
        return water;
    }

    public void setWater(Double water) {
        this.water = water;
    }
}